<template>
  <div class="restaurants" ref="restaurants">
    <div class="seller-content">
      <div class="overview">
        <h1 class="title">{{seller.name}}</h1>
        <div class="desc border-1px">
          <ratingstar :size="36" :score="seller.score"></ratingstar>
          <span class="text">{{seller.ratingCount}}</span>
          <span class="text">{{seller.sellCount}}</span>
        </div>
        <ul class="remark">
          <li class="block">
            <h2>起送价</h2>
            <div class="content">
              $<span class="stress">{{seller.minPrice}}</span>
            </div>
          </li>
          <li class="block">
            <h2>Delivery Price</h2>
            <div class="content">
              $<span class="stress">{{seller.deliveryPrice}}</span>
            </div>
          </li>
          <li class="block">
            <h2>Delivery Time</h2>
            <div class="content">
              <span class="stress">${{seller.deliveryTime}} </span>min
            </div>
          </li>
        </ul>
      </div>
      <bar></bar>
      <div class="bulletin">
        <h1 class="title">Anouncement</h1>
        <div class="content-wrapper border-1px">
          <p class="content">{{seller.bulletin}}</p>
        </div>
        <ul v-if="seller.supports" class="supports">
          <li class="support-item border-1px" v-for="(item, idx) in seller.supports">
            <span class="icon" :class="classMap[seller.supports[idx].type]"></span>
            <span class="text">{{seller.supports[idx].description}}</span>
          </li>
        </ul>
      </div>
      <bar></bar>
      <div class="img">
        <h1 class="title">Live Images</h1>
        <div class="img-wrapper" ref="imgWrapper">
          <ul class="img-list" ref="imgList">
            <li class="img-item" v-for="img in seller.pics">
              <img :src="img" width="120" height="90">
            </li>
          </ul>
        </div>
      </div>
    </div>
  </div>
</template>

<script type="text/ecmascript-6">
  import ratingstar from '../../components/ratingstar/ratingstar';
  import bar from '../../components/bar/bar';
  import BScroll from 'better-scroll';
  export default {
    props: {
      seller: {
        type: Object
      }
    },
    created() {
      this.classMap = ['decrease', 'discount', 'special', 'invoice', 'guarantee'];
    },
    mounted() {
      this.$nextTick(() => {
        this._initScroll();
        this._initialImgs();
      });
    },
    watch: {
      'seller'() {
        this.$nextTick(() => {
          this._initScroll();
          this._initialImgs();
        });
      }
    },
    methods: {
      _initScroll() {
        if (!this.scroll) {
          this.scroll = new BScroll(this.$refs.restaurants, {
            click: true
          });
        } else {
          this.scroll.refresh();
        }
      },
      _initialImgs() {
        if (this.seller.pics) {
          let imgWidth = 120;
          let margin = 6;
          let width = (imgWidth + margin) * this.seller.pics.length - margin;
          this.$refs.imgList.style.width = width + 'px';
          this.$nextTick(() => {
            if (!this.imgScroll) {
              this.imgScroll = new BScroll(this.$refs.imgWrapper, {
                scrollX: true,
                eventPassthrough: 'vertical'
              });
            } else {
              this.imgScroll.refresh();
            }
          });
        }
      }
    },
    components: {
      ratingstar,
      bar
    }
  };
</script>

